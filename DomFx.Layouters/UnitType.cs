namespace DomFx.Layouters
{
    /// <summary>
    ///   Specifies the measure of an Unit object.
    /// </summary>
    public enum UnitType
    {
        /// <summary>
        ///   Measure is in points. A point represents 1/72 of an inch.
        /// </summary>
        Point = 0,
        Centimeter = 1,
        Inch = 2,
    }
}